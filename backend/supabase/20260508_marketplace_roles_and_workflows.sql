DROP POLICY IF EXISTS marketplace_profiles_self_read ON zippo.marketplace_profiles;
CREATE POLICY marketplace_profiles_self_read
ON zippo.marketplace_profiles FOR SELECT TO authenticated
USING (
  zippo.is_admin()
  OR (auth.uid() IS NOT NULL AND auth.uid() = auth_user_id)
);

DROP POLICY IF EXISTS marketplace_profiles_self_write ON zippo.marketplace_profiles;
CREATE POLICY marketplace_profiles_self_write
ON zippo.marketplace_profiles FOR ALL TO authenticated
USING (
  zippo.is_admin()
  OR (auth.uid() IS NOT NULL AND auth.uid() = auth_user_id)
)
WITH CHECK (
  zippo.is_admin()
  OR (auth.uid() IS NOT NULL AND auth.uid() = auth_user_id)
);


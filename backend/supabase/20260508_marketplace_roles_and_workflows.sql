BEGIN;

CREATE SCHEMA IF NOT EXISTS zippo;

CREATE OR REPLACE FUNCTION zippo.is_admin()
RETURNS boolean
LANGUAGE sql
STABLE
AS $$
  SELECT
    (auth.jwt()->'app_metadata'->>'role' = 'admin')
    OR (auth.jwt()->'user_metadata'->>'role' = 'admin')
    OR (COALESCE(LOWER(auth.jwt()->'app_metadata'->>'is_admin'), '') = 'true')
    OR (COALESCE(LOWER(auth.jwt()->'user_metadata'->>'is_admin'), '') = 'true');
$$;

CREATE OR REPLACE FUNCTION zippo.jwt_role()
RETURNS text
LANGUAGE sql
STABLE
AS $$
  SELECT COALESCE(
    auth.jwt()->'app_metadata'->>'role',
    auth.jwt()->'user_metadata'->>'role',
    'guest'
  );
$$;

CREATE TABLE IF NOT EXISTS zippo.marketplace_profiles (
  profile_id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  auth_user_id uuid,
  user_id bigint UNIQUE,
  role text NOT NULL DEFAULT 'buyer' CHECK (role IN ('buyer', 'store_owner', 'driver', 'admin')),
  full_name text NOT NULL,
  email text NOT NULL,
  phone text,
  barangay text,
  address_line text,
  is_active boolean NOT NULL DEFAULT true,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS zippo.store_owner_applications (
  application_id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  applicant_user_id bigint NOT NULL,
  full_name text NOT NULL,
  email text NOT NULL,
  contact_no text NOT NULL,
  bir_tin text NOT NULL,
  dti_registration_no text NOT NULL,
  business_name text NOT NULL,
  business_address text NOT NULL,
  barangay text NOT NULL,
  documents jsonb NOT NULL DEFAULT '{}'::jsonb,
  status text NOT NULL DEFAULT 'pending_review' CHECK (status IN ('pending_review', 'approved', 'rejected')),
  moderation_note text,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS zippo.stores (
  store_id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  owner_user_id bigint NOT NULL,
  store_name text NOT NULL,
  description text,
  barangay text NOT NULL,
  lat double precision CHECK (lat IS NULL OR lat BETWEEN -90 AND 90),
  lng double precision CHECK (lng IS NULL OR lng BETWEEN -180 AND 180),
  is_active boolean NOT NULL DEFAULT true,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS zippo.marketplace_products (
  product_id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  store_id bigint NOT NULL REFERENCES zippo.stores(store_id) ON DELETE CASCADE,
  owner_user_id bigint NOT NULL,
  name text NOT NULL,
  description text,
  category text NOT NULL DEFAULT 'gift',
  price numeric(10, 2) NOT NULL CHECK (price >= 0),
  stock integer NOT NULL DEFAULT 0 CHECK (stock >= 0),
  occasion_tags text[] NOT NULL DEFAULT '{}'::text[],
  recipient_tags text[] NOT NULL DEFAULT '{}'::text[],
  tags text[] NOT NULL DEFAULT '{}'::text[],
  delivery_zones text[] NOT NULL DEFAULT '{}'::text[],
  local_vendor boolean NOT NULL DEFAULT true,
  avg_rating numeric(2, 1) CHECK (avg_rating IS NULL OR avg_rating BETWEEN 1.0 AND 5.0),
  popularity_score double precision NOT NULL DEFAULT 0 CHECK (popularity_score BETWEEN 0 AND 1),
  store_name text,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS zippo.buyer_orders (
  order_id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  buyer_user_id bigint NOT NULL,
  occasion text NOT NULL,
  recipient_type text NOT NULL,
  notes text,
  gift_pack jsonb NOT NULL DEFAULT '{}'::jsonb,
  delivery jsonb NOT NULL DEFAULT '{}'::jsonb,
  subtotal numeric(10, 2) NOT NULL DEFAULT 0 CHECK (subtotal >= 0),
  gift_pack_fee numeric(10, 2) NOT NULL DEFAULT 0 CHECK (gift_pack_fee >= 0),
  delivery_fee numeric(10, 2) NOT NULL DEFAULT 0 CHECK (delivery_fee >= 0),
  total_price numeric(10, 2) NOT NULL DEFAULT 0 CHECK (total_price >= 0),
  status text NOT NULL DEFAULT 'pending' CHECK (status IN ('pending', 'paid', 'processing', 'ready_for_pickup', 'in_transit', 'delivered', 'cancelled', 'refunded')),
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS zippo.buyer_order_items (
  order_item_id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  order_id bigint NOT NULL REFERENCES zippo.buyer_orders(order_id) ON DELETE CASCADE,
  product_id bigint,
  name text NOT NULL,
  quantity integer NOT NULL CHECK (quantity > 0),
  unit_price numeric(10, 2) NOT NULL CHECK (unit_price >= 0),
  line_total numeric(10, 2) NOT NULL CHECK (line_total >= 0),
  created_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS zippo.fraud_reports (
  report_id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  reporter_user_id bigint NOT NULL,
  accused_role text NOT NULL CHECK (accused_role IN ('buyer', 'store_owner', 'driver')),
  accused_user_id bigint,
  accused_store_id bigint,
  reason text NOT NULL,
  details text,
  evidence_url text,
  status text NOT NULL DEFAULT 'pending' CHECK (status IN ('pending', 'reviewing', 'resolved', 'dismissed')),
  moderation_note text,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS zippo.driver_tasks (
  task_id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  driver_user_id bigint NOT NULL,
  order_id bigint,
  pickup_label text,
  dropoff_label text,
  status text NOT NULL DEFAULT 'assigned' CHECK (status IN ('assigned', 'picked_up', 'in_transit', 'delivered', 'failed', 'cancelled')),
  last_note text,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);


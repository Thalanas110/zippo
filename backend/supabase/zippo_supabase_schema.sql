-- =========================
-- MODULE 1: GIFT INTELLIGENCE FILTER STORAGE
-- =========================

CREATE TABLE IF NOT EXISTS zippo.gift_filter_runs (
    run_id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
    user_id integer REFERENCES zippo.users(user_id)
        ON UPDATE CASCADE
        ON DELETE SET NULL,
    occasion text,
    recipient_type text,
    budget_min numeric(10, 2) CHECK (budget_min IS NULL OR budget_min >= 0),
    budget_max numeric(10, 2) CHECK (budget_max IS NULL OR budget_max >= 0),
    location_text text,
    preferences text[] NOT NULL DEFAULT '{}'::text[],
    avoid_terms text[] NOT NULL DEFAULT '{}'::text[],
    strict_budget boolean NOT NULL DEFAULT true,
    require_stock boolean NOT NULL DEFAULT true,
    require_delivery_zone boolean NOT NULL DEFAULT false,
    min_rating numeric(2, 1) CHECK (min_rating IS NULL OR min_rating BETWEEN 1.0 AND 5.0),
    config jsonb NOT NULL DEFAULT '{}'::jsonb,
    created_at timestamptz NOT NULL DEFAULT now(),
    CHECK (budget_min IS NULL OR budget_max IS NULL OR budget_min <= budget_max)
);

CREATE TABLE IF NOT EXISTS zippo.gift_filter_results (
    run_id uuid NOT NULL REFERENCES zippo.gift_filter_runs(run_id)
        ON UPDATE CASCADE
        ON DELETE CASCADE,
    product_id integer NOT NULL REFERENCES zippo.products(product_id)
        ON UPDATE CASCADE
        ON DELETE CASCADE,
    result_rank integer NOT NULL CHECK (result_rank > 0),
    score double precision NOT NULL CHECK (score BETWEEN 0 AND 1),
    score_parts jsonb NOT NULL DEFAULT '{}'::jsonb,
    passed_filter boolean NOT NULL DEFAULT true,
    filter_reasons text[] NOT NULL DEFAULT '{}'::text[],
    explanation text[] NOT NULL DEFAULT '{}'::text[],
    method text NOT NULL DEFAULT 'gift_intelligence_filter',
    created_at timestamptz NOT NULL DEFAULT now(),
    PRIMARY KEY (run_id, product_id),
    UNIQUE (run_id, result_rank)
);


CREATE OR REPLACE VIEW zippo.v_gifts_for_python AS
SELECT
    p.product_id AS id,
    p.name,
    p.description,
    p.price::double precision AS price,
    p.category::text AS category,
    p.vendor_id,
    p.vendor_name,
    p.stock,
    p.avg_rating::double precision AS rating,
    p.popularity_score AS popularity,
    p.local_vendor AS local,
    p.tags,
    p.occasion_tags AS occasions,
    p.recipient_tags AS recipients,
    p.delivery_zones,
    p.weight_score
FROM zippo.products p;

CREATE OR REPLACE VIEW zippo.v_riders_for_python AS
SELECT
    r.rider_id AS id,
    r.rider_name AS name,
    r.current_lat,
    r.current_lng,
    r.capacity,
    r.speed_kmph::double precision AS speed_kmph,
    r.average_rating::double precision AS average_rating,
    r.available
FROM zippo.riders r;

CREATE OR REPLACE VIEW zippo.v_orders_for_delivery_optimizer AS
SELECT
    o.order_id AS id,
    p.vendor_id,
    v.lat AS pickup_lat,
    v.lng AS pickup_lng,
    u.lat AS dropoff_lat,
    u.lng AS dropoff_lng,
    o.priority,
    o.promised_minutes,
    o.occasion,
    o.recipient_type,
    u.barangay AS dropoff_barangay,
    p.product_id,
    p.name AS product_name
FROM zippo.orders o
JOIN zippo.users u ON u.user_id = o.user_id
JOIN zippo.products p ON p.product_id = o.product_id
LEFT JOIN zippo.vendors v ON v.vendor_id = p.vendor_id
WHERE
    o.status IN ('pending', 'paid', 'processing')
    AND v.lat IS NOT NULL
    AND v.lng IS NOT NULL;


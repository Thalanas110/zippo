-- Optional normalization table for local vendors.
-- products.vendor_name is still kept because the original products.csv schema includes it.
CREATE TABLE IF NOT EXISTS zippo.vendors (
    vendor_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    vendor_name text NOT NULL UNIQUE,
    barangay text,
    lat double precision CHECK (lat IS NULL OR lat BETWEEN -90 AND 90),
    lng double precision CHECK (lng IS NULL OR lng BETWEEN -180 AND 180),
    is_local boolean NOT NULL DEFAULT true,
    created_at timestamptz NOT NULL DEFAULT now(),
    updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS zippo.users (
    user_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    full_name text NOT NULL,
    age_group zippo.age_group NOT NULL,
    gender zippo.gender_code NOT NULL,
    barangay text NOT NULL,
    budget_range zippo.budget_range NOT NULL,
    preferred_occasions text[] NOT NULL DEFAULT '{}'::text[],
    lat double precision NOT NULL CHECK (lat BETWEEN -90 AND 90),
    lng double precision NOT NULL CHECK (lng BETWEEN -180 AND 180),
    created_at date NOT NULL DEFAULT current_date,
    updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS zippo.products (
    product_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    vendor_id integer REFERENCES zippo.vendors(vendor_id)
        ON UPDATE CASCADE
        ON DELETE SET NULL,
    name text NOT NULL,
    description text,
    category zippo.product_category NOT NULL,
    price numeric(10, 2) NOT NULL CHECK (price >= 0),
    occasion_tags text[] NOT NULL DEFAULT '{}'::text[],
    recipient_tags text[] NOT NULL DEFAULT '{}'::text[],
    tags text[] NOT NULL DEFAULT '{}'::text[],
    delivery_zones text[] NOT NULL DEFAULT '{}'::text[],
    local_vendor boolean NOT NULL DEFAULT true,
    avg_rating numeric(2, 1) CHECK (avg_rating IS NULL OR avg_rating BETWEEN 1.0 AND 5.0),
    stock integer NOT NULL DEFAULT 0 CHECK (stock >= 0),
    vendor_name text NOT NULL,
    popularity_score double precision NOT NULL DEFAULT 0 CHECK (popularity_score BETWEEN 0 AND 1),
    weight_score double precision NOT NULL DEFAULT 0 CHECK (weight_score BETWEEN 0 AND 1),
    created_at timestamptz NOT NULL DEFAULT now(),
    updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS zippo.orders (
    order_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id integer NOT NULL REFERENCES zippo.users(user_id)
        ON UPDATE CASCADE
        ON DELETE CASCADE,
    product_id integer NOT NULL REFERENCES zippo.products(product_id)
        ON UPDATE CASCADE
        ON DELETE RESTRICT,
    occasion text NOT NULL,
    recipient_type text NOT NULL,
    rating smallint CHECK (rating IS NULL OR rating BETWEEN 1 AND 5),
    order_date date NOT NULL DEFAULT current_date,
    total_price numeric(10, 2) NOT NULL CHECK (total_price >= 0),
    status zippo.order_status NOT NULL DEFAULT 'completed',
    priority smallint NOT NULL DEFAULT 0 CHECK (priority BETWEEN 0 AND 10),
    promised_minutes integer NOT NULL DEFAULT 60 CHECK (promised_minutes > 0),
    created_at timestamptz NOT NULL DEFAULT now(),
    updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS zippo.riders (
    rider_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    rider_name text NOT NULL,
    barangay text,
    current_lat double precision NOT NULL CHECK (current_lat BETWEEN -90 AND 90),
    current_lng double precision NOT NULL CHECK (current_lng BETWEEN -180 AND 180),
    capacity integer NOT NULL DEFAULT 3 CHECK (capacity > 0),
    speed_kmph numeric(5, 2) NOT NULL DEFAULT 22.00 CHECK (speed_kmph > 0),
    average_rating numeric(2, 1) CHECK (average_rating IS NULL OR average_rating BETWEEN 1.0 AND 5.0),
    available boolean NOT NULL DEFAULT true,
    created_at timestamptz NOT NULL DEFAULT now(),
    updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS zippo.deliveries (
    delivery_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    order_id integer NOT NULL REFERENCES zippo.orders(order_id)
        ON UPDATE CASCADE
        ON DELETE CASCADE,
    rider_id integer REFERENCES zippo.riders(rider_id)
        ON UPDATE CASCADE
        ON DELETE SET NULL,
    barangay text NOT NULL,
    lat double precision NOT NULL CHECK (lat BETWEEN -90 AND 90),
    lng double precision NOT NULL CHECK (lng BETWEEN -180 AND 180),
    time_slot zippo.delivery_time_slot NOT NULL,
    distance_km numeric(5, 2) NOT NULL CHECK (distance_km >= 0),
    assigned_at timestamptz NOT NULL DEFAULT now(),
